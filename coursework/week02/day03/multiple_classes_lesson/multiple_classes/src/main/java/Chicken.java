public class Chicken {
    private String name;
    private int eggs;

    public Chicken(String name, int eggs) {
        this.name = name;
        this.eggs = eggs;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getEggs() {
        return eggs;
    }

    public void setEggs(int eggs) {
        this.eggs = eggs;
    }
}
